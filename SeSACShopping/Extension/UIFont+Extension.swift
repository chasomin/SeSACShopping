import UIKit


extension UIFont {
    static let small = UIFont.systemFont(ofSize: 13)
    static let smallBold = UIFont.boldSystemFont(ofSize: 13)

    static let regular = UIFont.systemFont(ofSize: 15)
    static let regularBold = UIFont.boldSystemFont(ofSize: 15)
    
    static let large = UIFont.systemFont(ofSize: 17)
    static let largeBold = UIFont.boldSystemFont(ofSize: 17)
}
